func tr(lang, key string) string {
    if m, ok := translations[lang]; ok {
        if v, ok2 := m[key]; ok2 {
            return v
        }
    }
    // fallback to en
    if m, ok := translations["en"]; ok {
        if v, ok2 := m[key]; ok2 {
            return v
        }
    }
    return key
}

func trf(lang, key string, a ...any) string {
    return fmt.Sprintf(tr(lang, key), a...)
}
